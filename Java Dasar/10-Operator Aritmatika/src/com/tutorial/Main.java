package com.tutorial;

public class Main {

    public static void main(String[] args) {

        int variabel1 = 10;
        int variabel2 = 5;
       int hasil;

        //Penjumlahan
        hasil = variabel1 + variabel2;
        System.out.println(variabel1  + " + " +  variabel2  + " = " + hasil);


        //Pengurangan
        hasil = variabel1 - variabel2;
        System.out.println(variabel1  + " - " +  variabel2  + " = " + hasil);


        //Perkalian
        hasil = variabel1 * variabel2;
        System.out.println(variabel1  + " * " +  variabel2  + " = " + hasil);


        //Pembagian
        hasil = variabel1 / variabel2;
        System.out.println(variabel1  + " / " +  variabel2  + " = " + hasil);

        //Modulus
        float a = 6;
        float b = 5;
        float hasil2;

        hasil2 = a  %  b;
        System.out.println(a  + " % " +  b  + " = " + hasil2);


    }
}
